const savedTheme = localStorage.getItem("theme");
const prefersDark = window.matchMedia("(prefers-color-scheme: dark)").matches;

function applyTheme(theme) {
  root.dataset.theme = theme;
  themeToggle.textContent = theme === "dark" ? "Light" : "Dark";
  localStorage.setItem("theme", theme);
}

applyTheme(savedTheme || (prefersDark ? "dark" : "light"));

themeToggle.addEventListener("click", () => {
  const nextTheme = root.dataset.theme === "dark" ? "light" : "dark";
  applyTheme(nextTheme);
});

function updateReadingProgress() {
  const scrollTop = window.scrollY;
  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
  const progress = maxScroll > 0 ? (scrollTop / maxScroll) * 100 : 0;
  progressBar.style.width = `${progress}%`;
  backToTop.classList.toggle("is-visible", scrollTop > 560);
}

function calculateReadingTime() {
  const words = article.innerText.trim().split(/\s+/).length;
  const minutes = Math.max(1, Math.ceil(words / 220));
  const activeContent = content[root.lang === "hi" ? "hi" : "en"];
  readingTime.textContent = `${minutes} ${activeContent.minuteLabel}`;
}

function setText(id, value) {
  const element = document.getElementById(id);
  if (element) element.textContent = value;
}

function updateMetaDescription(description) {
  const meta = document.querySelector('meta[name="description"]');
  if (meta) meta.setAttribute("content", description);
}

function applyLanguage(language) {
  const activeContent = content[language];
  root.lang = activeContent.lang;
  document.title = activeContent.title;
  updateMetaDescription(activeContent.description);
  article.innerHTML = activeContent.article;

  setText("hero-title", activeContent.title);
  setText("heroEyebrow", activeContent.eyebrow);
  setText("heroSummary", activeContent.summary);
  setText("authorMeta", activeContent.author);
  setText("dateMeta", activeContent.date);
  setText("navRead", activeContent.read);
  setText("toc-title", activeContent.toc.title);
  setText("tocSummary", activeContent.toc.summary);
  setText("tocIntroduction", activeContent.toc.introduction);
  setText("tocMind", activeContent.toc.mind);
  setText("tocMultiverse", activeContent.toc.multiverse);
  setText("tocTime", activeContent.toc.time);
  setText("tocDreams", activeContent.toc.dreams);
  setText("tocInnovation", activeContent.toc.innovation);
  setText("tocKnowledge", activeContent.toc.knowledge);
  setText("tocLimits", activeContent.toc.limits);
  languageToggle.textContent = activeContent.button;
  localStorage.setItem("language", language);
  calculateReadingTime();
  updateReadingProgress();
}

languageToggle.addEventListener("click", () => {
  const nextLanguage = root.lang === "hi" ? "en" : "hi";
  applyLanguage(nextLanguage);
});

backToTop.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

window.addEventListener("scroll", updateReadingProgress, { passive: true });
window.addEventListener("resize", updateReadingProgress);

